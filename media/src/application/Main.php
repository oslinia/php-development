<?php

namespace Application;

use PHPFrame\Frame\Path;
use PHPFrame\Frame\View;

use function PHPFrame\url_for;

class Main extends View
{
    public function __invoke(): array
    {
        return parent::render_template('main.php');
    }

    public function media(Path $path): array
    {
        return parent::render_media($path->name);
    }

    public function redirect(Path $path): array
    {
        $page_url = url_for('page', name: $path->name);

        return parent::redirect_response($page_url);
    }
}
